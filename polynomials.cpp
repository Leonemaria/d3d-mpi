double dJacobi(int n, int A, int B, double x)
{
    double d;
    if(n==0) {d=0.;}
    else
    {
        d=sqrt(n*(n+A+B+1.))*jacobi(n-1,A+1,B+1,x);
    }
    return d;  
}
