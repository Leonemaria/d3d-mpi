double jacobi(int n, int A, int B, double x)
{
    double an(int n, int A, int B); double bn(int n, int A, int B);
    double P;
    if(n==0) {P=sqrt(pow(2,-A-B-1)*std::tgamma(A+B+2)/(std::tgamma(A+1)*std::tgamma(B+1)));}
    else
    {
        if (n==1)
        {
            P=0.5*jacobi(0,A,B,x)*sqrt((A+B+3.)/((A+1.)*(B+1.)))*((A+B+2.)*x+A-B);
        }
        else
        {
            P=((x-bn(n-1,A,B))*jacobi(n-1,A,B,x)-an(n-1,A,B)*jacobi(n-2,A,B,x))/an(n,A,B);
        }
    }
    return P;  
}
double an(int n,int A, int B)
{
    return 2.*std::sqrt(n*(n+A+B)*(n+A)*(n+B)/((2.*n+A+B-1.)*(2.*n+A+B+1.)))/(2.*n+A+B);
}
double bn(int n,int A, int B)
{
    return -(A*A-B*B)/((2*n+A+B)*(2*n+A+B+2.));
}
vector2D l_to_xy(double l1,double l2, double l3, vector2D v[])
{
    return l3*v[0]+l2*v[1]+l1*v[2];
}
double psi3D(int i, int j, int k, vector3D rP)
{
    double a, b, c=rP[2];
    if (std::abs(rP[1]+rP[2])> 0.0000000001) {a=-2.*(1.+rP[0])/(rP[1]+rP[2])-1.;}
    else {a=-1.;}
    if (std::abs(1.-rP[2])> 0.0000000001) {b=2.*(1.+rP[1])/(1.-rP[2])-1.;}
    else {b=-1.;}
    return 2.*std::sqrt(2.)*jacobi(i,0,0,a)*jacobi(j,2*i+1,0,b)*jacobi(k,2*i+2*j+2,0,c)*power(b,i)*power(c,i+j);
}
double dPsi3Ddr(int i, int j, int k, vector3D rP)
{
    double a, b, c=rP[2], d;
    if (std::abs(rP[1]+rP[2])> 0.0000000001) {a=-2.*(1.+rP[0])/(rP[1]+rP[2])-1.;}
    else {a=-1.;}
    if (std::abs(1.-rP[2])> 0.0000000001) {b=2.*(1.+rP[1])/(1.-rP[2])-1.;}
    else {b=-1.;}
    if (i==0) {d=0.;}
    else
    {
        d=8.*sqrt(2.)*dJacobi(i,0,0,a)*jacobi(j,2*i+1,0,b)*jacobi(k,2*i+2*j+2,0,c)*power(b,i-1)*power(c,i+j-1);
    }
    return d;
}
double dPsi3Dds(int i, int j, int k, vector3D rP)
{
    double a, b, c=rP[2], d;
    if (std::abs(rP[1]+rP[2])> 0.0000000001) {a=-2.*(1.+rP[0])/(rP[1]+rP[2])-1.;}
    else {a=-1.;}
    if (std::abs(1.-rP[2])> 0.0000000001) {b=2.*(1.+rP[1])/(1.-rP[2])-1.;}
    else {b=-1.;}
    if (i==0)
    {
       if(j==0) {d=0.;}
       else {d=4.*sqrt(2.)*jacobi(0,0,0,a)*jacobi(k,2*j+2,0,c)*dJacobi(j,1,0,b)*power(c,j-1);}
    }
    else
    {
        d=4.*(a+1.)*sqrt(2.)*dJacobi(i,0,0,a)*jacobi(j,2*i+1,0,b)*jacobi(k,2*i+2*j+2,0,c)*power(b,i-1)*power(c,i+j-1)+
          4.*std::sqrt(2)*jacobi(i,0,0,a)*jacobi(k,2*i+2*j+2,0,c)*power(c,i+j-1)*(dJacobi(j,2*i+1,0,b)*power(b,i)-jacobi(j,2*i+1,0,b)*i*power(b,i-1));
    }
    return d;
}
double dPsi3Ddt(int i, int j, int k, vector3D rP)
{
    double a, b, c=rP[2], d;
    if (std::abs(rP[1]+rP[2])> 0.0000000001) {a=-2.*(1.+rP[0])/(rP[1]+rP[2])-1.;}
    else {a=-1.;}
    if (std::abs(1.-rP[2])> 0.0000000001) {b=2.*(1.+rP[1])/(1.-rP[2])-1.;}
    else {b=-1.;}
    if (i==0)
    {
       if(j==0) {d=2.*sqrt(2.)*jacobi(0,0,0,a)*jacobi(0,1,0,b)*dJacobi(k,2,0,c);}
       else
       {
           d=2.*(b+1.)*sqrt(2.)*jacobi(0,0,0,a)*jacobi(k,2*j+2,0,c)*dJacobi(j,1,0,b)*power(c,j-1)+
             2.*std::sqrt(2)*jacobi(0,0,0,a)*jacobi(j,1,0,b)*(dJacobi(k,2*j+2,0,c)*power(c,j)-jacobi(k,2*j+2,0,c)*j*power(c,j-1));
       }
    }
    else
    {
        d=4.*(a+1.)*sqrt(2.)*dJacobi(i,0,0,a)*jacobi(j,2*i+1,0,b)*jacobi(k,2*i+2*j+2,0,c)*power(b,i-1)*power(c,i+j-1)+
          2.*(b+1)*sqrt(2)*jacobi(i,0,0,a)*jacobi(k,2*i+2*j+2,0,c)*power(c,i+j-1)*(dJacobi(j,2*i+1,0,b)*power(b,i)-jacobi(j,2*i+1,0,b)*i*power(b,i-1))+
          2.*std::sqrt(2.)*jacobi(i,0,0,a)*jacobi(j,2*i+1,0,b)*power(b,i)*(dJacobi(k,2*i+2*j+2,0,c)*power(c,i+j)-jacobi(k,2*i+2*j+2,0,c)*(i+j)*power(c,i+j-1));
    }
    return d;
}
int nModes(int N)
{
    return (N+1)*(N+2)*(N+3)/6;
}
double power(double b, int i)  // compute (1-b)^i
{
    double p;
    if (i==0) {p=1.;}
    else {if (1.-b<=0.) {p=0.;} else {p=std::pow(1.-b,i);}} 
    return p;  
}
